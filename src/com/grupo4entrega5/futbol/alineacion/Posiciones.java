package com.grupo4entrega5.futbol.alineacion;

/**
 * El <code>enum</code> <code>Posiciones</code> representa las posiciones que
 * puede tener un jugador en un equipo de fútbol.
 * Cada jugador puede tener una de las siguientes posiciones:
 * <ul>
 * <li><code>Portero</code>: El jugador que está encargado de defender la
 * portería.</li>
 * <li><code>Defensa</code>: El jugador que juega en la línea defensiva del
 * equipo.</li>
 * <li><code>CentroCampista</code>: El jugador que juega en el centro del
 * campo.</li>
 * <li><code>Delantero</code>: El jugador que se especializa en marcar
 * goles.</li>
 * </ul>
 * 
 * <p>
 * Este <code>enum</code> permite representar de manera sencilla las distintas
 * posiciones posibles de un jugador en el campo de fútbol.
 * </p>
 * 
 * @author [Tu nombre]
 * @version 1.0
 */
public enum Posiciones {
    /**
     * Representa la posición de portero.
     */
    Portero,

    /**
     * Representa la posición de defensa.
     */
    Defensa,

    /**
     * Representa la posición de centrocampista.
     */
    CentroCampista,

    /**
     * Representa la posición de delantero.
     */
    Delantero
}
